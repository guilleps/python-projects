#creando tuplas con tuple() -> dentro se pasa una lista para convertir en tupla
tupla = tuple(["n1","n2"])

#creando una tupla sin parentesis de multiples datos
tupla = "n1","n2"

#creando una tupla sin parentesis de un solo dato, con ',' sino se vuelve un string comun
tupla = "n1",

print(tupla)

#cuando solo leeremos datos, creamos tuplas de datos fijos