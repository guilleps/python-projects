#pedirle un numero al usuario
numero = input("Ingrese numero: ")

#convierto el numero a entero -> int y multiplico por 2
resultado_entero = int(numero) * 2

#convierto el numero a flotante -float> y multiplico por 2, trabaja con decimales
resultado_flotante = float(numero) * 2

#mostrando el dato ingresado
print(resultado_flotante + 3)